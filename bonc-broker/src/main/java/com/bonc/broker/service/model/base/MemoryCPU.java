package com.bonc.broker.service.model.base;

/**
 * @author LYX
 *
 */
public class MemoryCPU {
    private String cpu;
    private String memory;

    /**
     * @return the cpu
     */
    public String getCpu() {
        return cpu;
    }

    /**
     * @param cpu
     *            the cpu to set
     */
    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    /**
     * @return the memory
     */
    public String getMemory() {
        return memory;
    }

    /**
     * @param memory
     *            the memory to set
     */
    public void setMemory(String memory) {
        this.memory = memory;
    }

}
